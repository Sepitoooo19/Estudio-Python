


print('Operadores Logicos')

print("AND (&)")

resultado = True & True     #Retorna True
resultado2 = True & False   #Retorna False
resultado3 = False & True   #Retorna False
resultado4 = False & False  #Retorna False

print(resultado, " = True & True")
print(resultado2, " = True & False")
print(resultado3, " = False & True")
print(resultado4, " = False & False")
print("")

print("OR (|)")

resultado5 = True | True    #Retorna True
resultado6 = True | False   #Retorna True
resultado7 = False | True   #Retorna True
resultado8 = False | False  #Retorna False

print(resultado5, " = True & True")
print(resultado6, " = True & False")
print(resultado7, " = False & True")
print(resultado8, " = False & False")
print("")

print("NOT (not)")
resultado9 = not True       # Retorna False
resultado10 = not False     # Retorna True
print(resultado9, " = not True")
print(resultado10, " = not False")