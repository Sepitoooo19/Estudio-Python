
print("Datos a = 5, b = 4")
a = 5
b = 4

print("Operador 'es igual que' (==), a = 5, b = 4")
print(a == b)
print("")

print("Operador 'es distinto de'(!=), a = 5, b = 4 ")
print(a != b)
print("")

print("Operador 'es menor que'(<), a = 5, b = 4 ")
print(a < b)
print("")

print("Operador 'es menor o igual que'(<=), a = 5, b = 4 ")
print(a <= b)
print("")

print("Operador 'es mayor que'(>), a = 5, b = 4 ")
print(a > b)
print("")

print("Operador 'es mayor o igual que'(>=), a = 5, b = 4 ")
print(a >= b)
print("")

